"""
Тесты slug-валидации для переименования проектов.

Regex: ^[a-z0-9][a-z0-9-]{0,40}[a-z0-9]$
(Используется в api_project_rename — если функция ещё не вынесена в константу,
тест дублирует regex: это намеренно, чтобы зафиксировать поведение и не сломать молча.)

Если webapp.py экспортирует _SLUG_RE — используем его напрямую.
Иначе — тестируем паттерн напрямую (backward-compatible).
"""
import re
import pytest


# Попытаться импортировать константу из webapp (лид мог добавить _SLUG_RE).
# Если не экспортирована — дублируем паттерн в тесте.
try:
    from webapp import _SLUG_RE  # type: ignore[attr-defined]
    _SLUG_PATTERN = _SLUG_RE
except ImportError:
    _SLUG_PATTERN = re.compile(r"^[a-z0-9][a-z0-9-]{0,40}[a-z0-9]$")


def _slug_ok(slug: str) -> bool:
    """Возвращает True если slug валиден по паттерну."""
    return bool(_SLUG_PATTERN.match(slug))


# ─────────────────────────── валидные слаги ───────────────────────────

@pytest.mark.parametrize("slug", [
    "my-cool-bot",
    "abc-def-ghi",
    "claude-ops-bot",
    "a1",
    "z9",
    "project1",
    "my-project-2",
    "networking-os",
    "a1b2c3d4e5f6",
])
def test_slug_valid(slug: str):
    """Корректные слаги должны проходить валидацию."""
    assert _slug_ok(slug), f"Slug {slug!r} должен быть валидным"


# ─────────────────────────── невалидные слаги ───────────────────────────

@pytest.mark.parametrize("slug,reason", [
    ("-leading",       "начинается с дефиса"),
    ("trailing-",      "заканчивается дефисом"),
    ("UPPER",          "заглавные буквы"),
    ("MyProject",      "смешанный регистр"),
    ("with_underscore","подчёркивание не допускается"),
    ("space here",     "пробел в середине"),
    ("",               "пустая строка"),
    ("-",              "только дефис"),
    ("a",              "один символ — не соответствует min length (нужен [a-z0-9]{2} min)"),
    ("abc!def",        "спецсимвол восклицательный знак"),
    ("abc.def",        "точка не допускается"),
    ("abc/def",        "слеш не допускается"),
    ("a" * 43,         "слишком длинный (>42 символов)"),
])
def test_slug_invalid(slug: str, reason: str):
    """Невалидные слаги должны отвергаться."""
    assert not _slug_ok(slug), f"Slug {slug!r} должен быть НЕВАЛИДНЫМ: {reason}"


# ─────────────────────────── граничные случаи ───────────────────────────

def test_slug_min_length():
    """Слаг из 2 символов — минимально допустимый (один символ уже нет)."""
    assert _slug_ok("a1"), "Слаг из 2 символов должен быть допустим"
    assert not _slug_ok("a"), "Слаг из 1 символа не должен быть допустим"


def test_slug_max_length():
    """42 символа — максимально допустимый (1 + 40 + 1)."""
    max_slug = "a" + "b" * 40 + "c"  # 42 символа
    assert len(max_slug) == 42
    assert _slug_ok(max_slug), f"Слаг из 42 символов должен быть допустим"

    too_long = "a" + "b" * 41 + "c"  # 44 символа
    assert not _slug_ok(too_long), "Слаг из 44 символов должен быть недопустим"


def test_slug_numbers_only():
    """Слаг только из цифр валиден."""
    assert _slug_ok("12"), "12 — допустимый слаг"
    assert _slug_ok("123456"), "Только цифры допустимы"


def test_slug_consecutive_dashes():
    """Несколько дефисов подряд — паттерн [a-z0-9-] допускает это, проверяем поведение."""
    # Паттерн [a-z0-9-]{0,40} допускает "--", это технически ok.
    # Важно задокументировать это поведение: тест фиксирует его явно.
    slug_with_double_dash = "my--project"
    # Это соответствует паттерну (начинается и заканчивается буквой/цифрой)
    assert _slug_ok(slug_with_double_dash), (
        "Двойной дефис технически допускается паттерном [a-z0-9-]{0,40} — "
        "тест документирует это поведение"
    )
